$result = $mysqli->query($sql);

if ($result->num_rows > 0) {
  // output data of each row
  while($row = $result->fetch_assoc()) {
    echo "id: " . $row["ID_Admin"]. " - Name: " . $row["First_Name"]. "<br>";
  }
} else {
  echo "0 results";
}
